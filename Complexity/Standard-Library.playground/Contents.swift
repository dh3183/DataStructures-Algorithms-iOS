import Foundation

// MARK: Array
/// Array Literal을 이용해 배열 생성함
var people = ["Brian", "Stanley", "Ringo"]

/// Order
people[0] // "Brian"
people[1] // "Stanley"
people[2] // "Ringo"

people.append("Charles")]

// MARK: Insertion location
people.insert("Andy", at: 0)
// ["Andy", "Brian", "Stanley", "Ringo", "Charles"]
